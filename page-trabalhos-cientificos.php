        <h2>Normas para a elaboração dos resumos simples</h2>
        <p>Fonte Times New Roman, tamanho 12, justificado, espaçamento entre linhas 1,5 linhas, parágrafo com recuo de 1,25 cm, margens de 25 mm e tamanho do papel A4 (210 x 297 mm).</p>

        <h3>Título</h3>
        <p>Informativo e breve, escrito em letras maiúsculas e negrito, centralizado, seguido do (s) nome (s) do (s) autor (es) e sua (s) respectiva (s) instituição (ões). E-mail para contato deve vir após as filiações.</p>
        <p>Nome científico, quando referido no título, deve ser escrito em itálico e vir acompanhado dos autores e do nível taxonômico ao qual é subordinado, entre parênteses.</p>

        <h3>Texto</h3>
        <ul>
            <li><p>Parágrafo único e com até 2.500 caracteres (incluindo espaços) e estruturado com introdução, metodologia, resultados, discussão e conclusões. O texto do resumo é contínuo e subtítulos, como “introdução”, “metodologia” etc, não devem ser incluídos. </p></li>
            <li><p>Não entram nessa contagem de caracteres o título, nome, e-mail e instituição de afiliação dos respectivos autores.</p></li>
            <li><p>Resumos com número de caracteres maior que 2.500, não serão aceitos.</p></li>
            <li><p>Nomes científicos (espécie e gênero) devem ser grafados em itálico.</p></li>
            <li><p>Nomes de espécies devem vir acompanhados do nome do respectivo autor.</p></li>
            <li><p>Outros grifos, tais como negrito ou sublinhado, não deverão ser utilizados.</p></li>
            <li><p>Tabelas, gráficos, citações ou referências bibliográficas, não deverão constar no texto.</p></li>
            <li><p>Instituições de fomento devem constar no final do texto, entre parêntesis.</p></li>
        </ul>

