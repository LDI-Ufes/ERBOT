        <h2>Normas para a elaboração dos resumos expandidos</h2>
        <p>Fonte Times New Roman, tamanho 12, justificado, espaçamento entre linhas 1,5 linhas, parágrafo com recuo de 1,25 cm, margens de 25 mm e tamanho do papel A4 (210 x 297 mm), máximo 5 páginas (sem as referências bibliográficas).</p>

        <h3>Título</h3>
        <p>Informativo e breve, escrito em letras maiúsculas e negrito, centralizado, seguido do (s) nome (s) do (s) autor (es) e sua (s) respectiva (s) instituição (ões). E-mail para contato deve vir após as filiações.</p>
        <p>Nome científico, quando referido no título, deve ser escrito em itálico e vir acompanhado dos autores e do nível taxonômico ao qual é subordinado, entre parênteses.</p>

        <h3>Texto</h3>
        <ul>
            <li><p>Itens em negrito e caixa alta: RESUMO, PALAVRAS-CHAVE, INTRODUÇÃO, MATERIAL E MÉTODOS, RESULTADOS E DISCUSSÃO (podem ser escritos em um único tópico), CONCLUSÃO, AGRADECIMENTOS (opcional) e REFERÊNCIAS.</p></li>
            <li><p>Subitens em negrito e caixa baixa. Inserir um espaço entre linhas antes de cada item e/ou subitem quando presente.</p></li>
            <li><p>Nomes científicos (espécie e gênero) devem ser grafados em itálico.</p></li>
            <li><p>Nomes de espécies devem vir acompanhados do nome do respectivo autor.</p></li>
            <li><p>Outros grifos, tais como negrito ou sublinhado, não deverão ser utilizados.</p></li>
            <li><p>O resumo deve seguir as orientações do texto para resumo simples.</p></li>
        </ul>

        <h3>Elementos gráficos</h3>
        <ul>
            <li><p>As figuras e tabelas devem ser apresentadas ao longo do texto, conforme a ordem de sua citação e estarem devidamente numeradas e com legendas. Obedecer à proporção a ser ocupada na página de tamanho A4, não sendo permitidas quebra de páginas ou seção para inserção de elementos gráficos. </p></li>
            <li><p>Para resumos com muitas figuras, sugere-se a montagem pranchas.</p></li>
            <li><p>As legendas devem ser suficientemente completas, permitindo o entendimento independente de cada figura ou tabela.</p></li>
            <li><p>As legendas das tabelas deverão estar posicionadas na parte superior. As legendas das figuras deverão estar abaixo.</p></li>
            <li><p>A numeração das figuras deve ser independente da numeração das tabelas e ambas devem utilizar algarismos arábicos, seguindo a ordem em que foram citadas no texto.</p></li>
            <li><p>Fotos e pranchas devem ter uma resolução mínima de 300 dpi (pontos por polegada).</p></li>
            <li><p>Gráficos devem ter uma resolução mínima de 600 dpi.</p></li>
        </ul>

